package Ejercicio4;

public class Ejercicio4 {

	public static void main(String[] args) {
		byte b=32;
		System.out.println("El valor de la variable de tipo byte (b) es: " + b);
		short s=558;
		System.out.println("El valor de la variable de tipo short (s) es: " + s);
		int i=143646;
		System.out.println("El valor de la variable de tipo entero (i) es: " + i);
		char c='a';
		System.out.println("El valor de la variable de tipo caracter (c) es: " + c);
		double d=46.648;
		System.out.println("El valor de la variable de tipo número decimal (d) es: " + d);
		float f=4.1f;
		System.out.println("El valor de la variable de tipo número flotante (f) es: " + f);
		String str="Hola";
		System.out.println("El valor de la variable de tipo cadena de caracteres (str) es: " + str);
	}

}
